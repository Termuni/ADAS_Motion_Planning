function distance = Calc_Distance(point1, point2)
    % 입력:
    % point1: [x1, y1] 첫 번째 점의 좌표
    % point2: [x2, y2] 두 번째 점의 좌표
    %
    % 출력:
    % distance: 두 점 사이의 유클리드 거리

    dx = point2(1) - point1(1); % x 좌표 차이
    dy = point2(2) - point1(2); % y 좌표 차이

    distance = sqrt(dx^2 + dy^2);
end
